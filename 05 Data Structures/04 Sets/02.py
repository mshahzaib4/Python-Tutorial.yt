# Set Operations

set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

# Union
union_set = set1 | set2
print(union_set)

# Intersection
intersection_set = set1 & set2
print(intersection_set)

# Difference
difference_set = set1 - set2
print(difference_set)

# Symmetric Difference
symmetric_diff_set = set1 ^ set2
print(symmetric_diff_set)
